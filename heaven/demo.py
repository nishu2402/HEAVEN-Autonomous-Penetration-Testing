def _f(severity: str, vuln_type: str, title: str, *, target: str, risk: float,
       param: str = "", endpoint: str = "", cve: str = "", confidence: float = 0.9,
       description: str = "", impact: str = "", remediation: str = "",
       payload: str = "", method: str = "GET", cwe: str = "", owasp: str = "") -> dict:
    """Build one finding dict in the shape EngagementStore.upsert_finding wants."""
    bucket = "high" if confidence >= 0.8 else "medium" if confidence >= 0.5 else "low"
    return {
        "target": target, "vuln_type": vuln_type, "title": title,
        "severity": severity, "confidence": confidence, "confidence_bucket": bucket,
        "cve_id": cve, "risk_score": risk, "param": param, "endpoint": endpoint,
        "method": method,
        "evidence": {
            "parameter": param, "payload": payload, "method": method,
            "url": endpoint or target,
            "description": description, "impact": impact, "remediation": remediation,
            "cwe": cwe, "owasp": owasp,
            "note": "Sample finding generated by `heaven demo` — not from a real scan.",
        },
    }


