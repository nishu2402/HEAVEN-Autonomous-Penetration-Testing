def resolve_demo_store(name: Optional[str] = None):
    """Resolve the engagement store the web dashboard reads by default.

    Mirrors ``heaven.api.server._engagement_store_factory`` exactly so that data
    seeded from the CLI (`heaven demo`) and from the web ("Load sample data")
    land in the *same* SQLite file — i.e. ``<data_dir>/engagements/default.db``.
    """
    from heaven.config import get_config
    from heaven.engagement import EngagementStore

    data_dir = get_config().data_dir
    path = name or os.environ.get("HEAVEN_ENGAGEMENT") or "default"
    p = Path(path)
    if not p.suffix and not p.is_absolute() and "/" not in path and "\\" not in path:
        p = data_dir / "engagements" / f"{path}.db"
    return EngagementStore(p)


def seed_demo(store: Any) -> dict:
    """Idempotently seed ``store`` with the demo engagement + findings.

    Returns a summary: ``{engagement, scan_id, findings, by_severity, targets}``.
    Safe to call repeatedly — content-hashed IDs dedupe.
    """
    store.create_engagement(
        DEMO_ENGAGEMENT,
        client="ACME Corp (sample)",
        statement_of_work="Illustrative data generated by `heaven demo`. No real "
                          "systems were scanned.",
    )
    for target, kind, crit in [
        (WEB, "url", "high"),
        (f"{HOST}/32", "host", "crown_jewel"),
        ("10.10.10.0/24", "network", "medium"),
    ]:
        store.add_scope(target, kind=kind, criticality=crit, notes="sample scope")

    store.record_scan_start(DEMO_SCAN_ID, name="Demo web + network scan", mode="full")
    by_sev: dict[str, int] = {}
    for finding in _FINDINGS:
        store.upsert_finding(DEMO_SCAN_ID, finding)
        by_sev[finding["severity"]] = by_sev.get(finding["severity"], 0) + 1

    summary = {
        "findings": len(_FINDINGS),
        "by_severity": by_sev,
        "targets": 3,
        "duration_seconds": 47,
        "demo": True,
    }
    store.record_scan_complete(DEMO_SCAN_ID, summary)
    return {
        "engagement": DEMO_ENGAGEMENT,
        "scan_id": DEMO_SCAN_ID,
        "findings": len(_FINDINGS),
        "by_severity": by_sev,
        "targets": 3,
    }
